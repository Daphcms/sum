 class Suma {

    public static int sumar(int a, int b) {
        return a + b;
    }

    public static void main(String[] args) {
        int resultado1 = sumar(5, 3);
        int resultado2 = sumar(10, 20);

        System.out.println("La suma de 5 + 3 es: " + resultado1);
        System.out.println("La suma de 10 + 20 es: " + resultado2);
    }
}
//parametros
//1. int a(un numero de tipo entero denominado a)
//2. int b(un numero de tipo entero denominado b)